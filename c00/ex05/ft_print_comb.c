#include <unistd.h>

void	ft_print_comb(void)
{
	char	run;
	char	a[3];

	a[0] = 47;
	run = 1;
	write(1, "012", 3);
	while (a[0] < 55)
	{
		a[1] = 48;
		a[0]++;
		while (a[1] < 57)
		{
			a[2] = 50;
			a[1]++;
			while (a[2] < 57)
			{
				a[2]++;
				if (a[2] > a[1] && a[1] > a[0])
				{
					write(1, ",", 1);
					write(1, " ", 1);
					write(1, &a, 3);	
				}
			}
		}
	}
}

int	main(void)
{
	ft_print_comb();
}
