#include <unistd.h>

void	ft_print_comb(void)
{
	char	a[3];

	a[0] = 47;
	write(1, "012", 3);
	while (a[0]++ < 55)
	{
		a[1] = a[0];
		while (a[1]++ < 57)
		{
			a[2] = 50;
			while (a[2]++ < 57)
			{
				if (a[2] > a[1] && a[1] > a[0])
				{
					write(1, ", ", 2);
					write(1, &a, 3);
				}
			}
		}
	}
}
